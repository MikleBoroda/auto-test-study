package lesson3.task1;

public class Calculator {

    int summ(int term, int composer) {
        return term + composer;
    }

    int substract(int subtracted, int subtrahend) {
        return subtracted - subtrahend;
    }

    int multiply(int multiplied, int multiplier) {
        return multiplied * multiplier;
    }

    int divide(int dividend, int divider) {
        return dividend / divider;
    }

}
